package product;

public class Product {

	private Integer product_num;
	private String  product_name;
	private Integer product_value;
	private String product_image;
	private Integer product_amount;
	private String product_categoly;
	public Integer getProduct_num() {
		return product_num;
	}
	public void setProduct_num(Integer product_num) {
		this.product_num = product_num;
	}
	public String getProduct_name() {
		return product_name;
	}
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	public Integer getProduct_value() {
		return product_value;
	}
	public void setProduct_value(Integer product_value) {
		this.product_value = product_value;
	}
	public String getProduct_image() {
		return product_image;
	}
	public void setProduct_image(String product_image) {
		this.product_image = product_image;
	}
	public Integer getProduct_amount() {
		return product_amount;
	}
	public void setProduct_amount(Integer product_amount) {
		this.product_amount = product_amount;
	}
	public String getProduct_categoly() {
		return product_categoly;
	}
	public void setProduct_categoly(String product_categoly) {
		this.product_categoly = product_categoly;
	}
	
}
